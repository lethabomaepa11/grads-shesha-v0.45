import {mkdirSync, readdirSync, readFileSync, writeFileSync} from 'node:fs';
import path from 'node:path';

const projectRoot = process.cwd();
const docsRoot = path.join(projectRoot, 'docs');
const outputPath = path.join(projectRoot, 'src', 'data', 'generatedKnowledgeDocs.ts');
const supportedExtensions = new Set(['.md', '.mdx']);

function walkDocs(dirPath) {
  return readdirSync(dirPath, {withFileTypes: true}).flatMap((entry) => {
    const fullPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      return walkDocs(fullPath);
    }

    if (!supportedExtensions.has(path.extname(entry.name).toLowerCase())) {
      return [];
    }

    return [fullPath];
  });
}

function splitFrontmatter(rawContent) {
  const match = rawContent.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);

  if (!match) {
    return {frontmatter: {}, body: rawContent};
  }

  const frontmatter = Object.fromEntries(
    match[1]
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean)
      .map((line) => {
        const separatorIndex = line.indexOf(':');

        if (separatorIndex === -1) {
          return null;
        }

        const key = line.slice(0, separatorIndex).trim();
        const value = line.slice(separatorIndex + 1).trim().replace(/^['"]|['"]$/g, '');
        return [key, value];
      })
      .filter(Boolean),
  );

  return {
    frontmatter,
    body: rawContent.slice(match[0].length),
  };
}

function normalizeWhitespace(value) {
  return value.replace(/\r?\n/g, ' ').replace(/\s+/g, ' ').trim();
}

function stripMarkdown(markdown) {
  return normalizeWhitespace(
    markdown
      .replace(/```[\s\S]*?```/g, ' ')
      .replace(/`([^`]+)`/g, '$1')
      .replace(/!\[[^\]]*?\]\([^)]+\)/g, ' ')
      .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
      .replace(/^>\s?/gm, '')
      .replace(/^#{1,6}\s+/gm, '')
      .replace(/^\s*[-*+]\s+/gm, '')
      .replace(/^\s*\d+\.\s+/gm, '')
      .replace(/\|/g, ' ')
      .replace(/^\s*:?-{3,}:?\s*$/gm, ' ')
      .replace(/<[^>]+>/g, ' ')
      .replace(/[*_~]/g, '')
      .replace(/[^\S\r\n]+/g, ' '),
  );
}

function toTitleCase(value) {
  return value
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((segment) => segment.charAt(0).toUpperCase() + segment.slice(1))
    .join(' ');
}

function buildRoute(relativePath) {
  const noExtension = relativePath.replace(/\.(md|mdx)$/i, '');
  const normalized = noExtension.split(path.sep).join('/');
  return `/docs/${normalized}`;
}

function inferCategory(relativePath) {
  const segments = relativePath.split(path.sep);
  if (segments.length === 1) {
    return 'Docs';
  }

  return toTitleCase(segments[0]);
}

function inferTags(relativePath, textContent) {
  const pathTokens = relativePath
    .replace(/\.(md|mdx)$/i, '')
    .split(/[\\/._-]+/)
    .filter(Boolean)
    .map((token) => token.toLowerCase());

  const frequentTerms = Array.from(
    new Set(
      (textContent.toLowerCase().match(/\b[a-z][a-z0-9-]{3,}\b/g) ?? [])
        .filter((term) => !new Set(['this', 'that', 'with', 'from', 'into', 'your', 'have']).has(term))
        .slice(0, 12),
    ),
  );

  return Array.from(new Set([...pathTokens, ...frequentTerms])).slice(0, 12);
}

function inferSummary(textContent, fallbackTitle) {
  const sentences = textContent
    .split(/(?<=[.!?])\s+/)
    .map((sentence) => sentence.trim())
    .filter(Boolean);

  return sentences[0] ?? `${fallbackTitle} documentation.`;
}

const docs = walkDocs(docsRoot)
  .map((filePath) => {
    const relativePath = path.relative(docsRoot, filePath);
    const rawContent = readFileSync(filePath, 'utf8');
    const {frontmatter, body} = splitFrontmatter(rawContent);
    const textContent = stripMarkdown(body);
    const titleMatch = body.match(/^#\s+(.+)$/m);
    const title = frontmatter.title || titleMatch?.[1]?.trim() || toTitleCase(path.parse(relativePath).name);
    const category = inferCategory(relativePath);

    return {
      title,
      route: buildRoute(relativePath),
      category,
      tags: inferTags(relativePath, textContent),
      summary: inferSummary(textContent, title),
      content: textContent,
    };
  })
  .sort((left, right) => left.route.localeCompare(right.route));

const fileContents = `/* This file is auto-generated by scripts/generate-knowledge-docs.mjs. */
import type {KnowledgeDoc} from './sheshaKnowledge';

export const generatedKnowledgeDocs: KnowledgeDoc[] = ${JSON.stringify(docs, null, 2)} as KnowledgeDoc[];
`;

mkdirSync(path.dirname(outputPath), {recursive: true});
writeFileSync(outputPath, fileContents);
console.log(`Generated ${docs.length} knowledge docs at ${path.relative(projectRoot, outputPath)}`);
